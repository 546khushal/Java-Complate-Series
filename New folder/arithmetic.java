public class arithmetic {
    public static void main(String[] args) {

        int x, y, z;
        x = 20;
        y = 2;
        z = x + y;
        System.out.println("addition=" + z);

        z = x - y;
        System.out.println("subtraction=" + z);

        z = x * y;
        System.out.println("multipliction=" + z);

        z = x / y;
        System.out.println("division=" + z);

    }

}
