//Write a program to perform addition of two numbers and display its answer. (No need to take user input)
public class day2_1 {             //AdditionWithoutInput
    public static void main(String[] args) {
        int num1 = 10, num2 = 20;
        int sum = num1 + num2;
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
